#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	unsigned int		i;
	char				*s2;

	i = 0;
	s2 = (char *) malloc(sizeof(*s2) * ft_strlen(s) + 1);
	if (!s || !f)
		return (NULL);
	while (s[i])
	{
		s2[i] = (*f)(i, s[i]);
		i++;
	}
	s2[i] = '\0';
	return (s2);
}
