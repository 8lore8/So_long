#include "libft.h"

int	ft_countdec(int n)
{
	int			i;
	long int	g;

	g = n;
	if (g == 0)
		return (1);
	i = 0;
	if (g < 0)
	{
		g *= -1;
		i = 1;
	}
	while (g > 0)
	{
		i++;
		g = g / 10;
	}
	return (i);
}
